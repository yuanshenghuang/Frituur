    public ProductUpdate(Product product)
    {
        initComponents();
        _product = product;
        
       ArrayList<Category> list = CategoryService.selectAll();
     
       for(Category category:list)
       {
           jComboBox.addItem(category.getName());
           
       }
       Category category = CategoryService.selectOne(_product.getCategoryid());
       jComboBox.setSelectedItem(category.getName());
           
          
      
       
        txtNaam.setText(_product.getName());
        txtPrijs.setText(Double.toString(_product.getPrijs()));
            
        
        
        
       final int option = JOptionPane.showConfirmDialog(null, this, "", JOptionPane.OK_CANCEL_OPTION);    
       
       if(option == JOptionPane.OK_OPTION)
       {
        _product = new Product();
        
        _product.setName(txtNaam.getText());
        _product.setPrijs(Double.parseDouble(txtPrijs.getText()));
       
        Category ob=(Category) jComboBox.getSelectedItem();      
      
        _product.setCategoryid(ob.getId());
       }
       
       else if(option == JOptionPane.CANCEL_OPTION)
       {
           JFrameProduct jframeproduct = new JFrameProduct();
       }
    }

    private Product _product;
    
    public Product getProduct()
    {        
        return _product;
    }
