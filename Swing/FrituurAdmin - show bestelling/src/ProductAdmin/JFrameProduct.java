    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        product = (Product)lstProduct.getSelectedValue();
        
        ProductenService.delete(product.getID());
        
        list= ProductenService.selectAll();
        updateListBox();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (lstProduct.getSelectedIndex() != -1)
        {
            product = (Product)list.get(lstProduct.getSelectedIndex());
            if(JOptionPane.showConfirmDialog(null, "Wilt u lstProduct.getSelectedIndex()de geselecteerde product wijzigen","update", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
            {
                ProductUpdate update = new ProductUpdate(product);
                
                product = update.getProduct();
                product.setID( ((Product)lstProduct.getSelectedValue()).getID() );
                
                ProductenService.update(product);
                
                list = ProductenService.selectAll();
                updateListBox();
            }
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

